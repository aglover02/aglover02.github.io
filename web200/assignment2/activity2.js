let ageInYears = 22; 


let ageInMonths = ageInYears * 12; 
let ageInDays = ageInYears * 365; 
let ageInHours = ageInDays * 24; 
let ageInSeconds = ageInHours * 3600; 


document.write(`<p>Age in Years: ${ageInYears} year(s)</p>`);
document.write(`<p>Age in Months: ${ageInMonths} months</p>`);
document.write(`<p>Age in Days: ${ageInDays} days</p>`);
document.write(`<p>Age in Hours: ${ageInHours} hours</p>`);
document.write(`<p>Age in Seconds: ${ageInSeconds} seconds</p>`);